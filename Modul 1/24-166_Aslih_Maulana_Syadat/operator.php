<?php
$a = 15;
$b = 5;
$c = 10;
$d = 3;

// Penjumlahan
$hasil_tambah = $a + $b;
echo "Penjumlahan: $hasil_tambah <br>";

// Pengurangan
$hasil_kurang = $a - $b;
echo "Pengurangan: $hasil_kurang <br>";

// Perkalian
$hasil_kali = $a * $b;
echo "Perkalian: $hasil_kali <br>";

// Pembagian
$hasil_bagi = $a / $b;
echo "Pembagian: $hasil_bagi <br>";

// Modulus (Sisa Pembagian)
$hasil_modulus = $c % $d;
echo "Modulus: $hasil_modulus";
?>